package PRUEBA_NETFLIX;

public class Juego {
	private String nombre;
	private String genero;

	public Juego(String nombre, String genero) {
		this.nombre = nombre;
		this.genero = genero;
	}

	public String getNombre() {
		return nombre;
	}

	public String getGenero() {
		return genero;
	}

}
