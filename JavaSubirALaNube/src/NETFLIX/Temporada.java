package NETFLIX;

public class Temporada {

	private int Numero;
	private int Capitulos;

	public Temporada() {
	}

	public int getNumero() {
		return Numero;
	}

	public int getCapitulos() {
		return Capitulos;
	}

	public void getNumero(int Numero) {
		this.Numero = Numero;
	}

	public void getCapitulos(int Capitulos) {
		this.Capitulos = Capitulos;
	}
}
