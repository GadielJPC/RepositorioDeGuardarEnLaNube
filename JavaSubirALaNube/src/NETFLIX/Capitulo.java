package NETFLIX;

public class Capitulo {

	private int Numero;
	private int Duracion;

	public Capitulo() {
	}

	public int getNumero() {
		return Numero;
	}

	public int getDuracion() {
		return Duracion;
	}

	public void getNumero(int Numero) {
		this.Numero = Numero;
	}

	public void getDuracion(int Duracion) {
		this.Duracion = Duracion;
	}
}
